const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    age: {
        type: Number,
        required: true
    },
    mobile: {
        type: Number,
        required: true
    },
    work: {
        type: String,
        required: true
    },
    add: {
        type: String,
        required: true
    },
    desc: {
        type: String,
        required: true
    }
});

// const function name to be exported = model of mongoose("name of collection or db as given in cluster if different a new collection will be formed", name of Schema);
const User = new mongoose.model("users", userSchema);

module.exports = User;